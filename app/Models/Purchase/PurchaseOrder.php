#[ObservedBy([PurchaseOrderObserver::class])]
#[ScopedBy(TeamScope::class)]
#[ScopedBy(MemberScope::class)]
class PurchaseOrder extends BaseModel
{
    use Automationable;
    use HasMembers;
    use InteractsWithAttachments;
    use LogsActivity;
    use SoftDeletes;

    /**
     * The table associated with the model.
     *
     * @var string
     */
    protected $table = 'purchase_orders';

    protected static $howManyFake = 5;

    protected $fillable = [
        'number_seq',
        'team_id',
        'user_id',
        'uuid',
        'number',
        'order_model_id',
        'date',
        'contact_id',
        'vendor_id',
        'order_status_id',
        'request_shipping_date',
        'estimated_shipping_date',
        'actual_shipping_date',
        'estimated_delivery_date',
        'actual_delivery_date',
        'description',
        'terms',
        'payment_method_id',
        'total',
        'details',
    ];

    /**
     * Get Resource class
     */
    public function getResourceClass(): string
    {
        return PurchaseOrderResource::class;
    }

    protected $casts = [
        'details' => 'array',
    ];

    public function getJDetailsRawAttribute()
    {
        return $this->attributes['details'];
    }

    public function user(): BelongsTo
    {
        return $this->belongsTo(User::class);
    }

    public function team(): BelongsTo
    {
        return $this->belongsTo(Team::class);
    }

    /**
     * A Order BelongsTo PurchaseOrder Model
     */
    public function defaultModel(): BelongsTo
    {
        return $this->belongsTo(PurchaseOrderModel::class, 'order_model_id', 'id');
    }

    public function status(): BelongsTo
    {
        return $this->belongsTo(PurchaseOrderStatus::class, 'order_status_id');
    }

    /**
     * Get the stock entries generated by this purchase order.
     * An order can have multiple stock entries in case of partial shipments.
     */
    public function stockEntries(): HasMany
    {
        return $this->hasMany(PurchaseStockEntry::class, 'purchase_order_id');
    }

    /**
     * A Order BelongsTo Contact
     */
    public function contact(): BelongsTo
    {
        return $this->belongsTo(Contact::class);
    }

    /**
     * An PurchaseOrder belong to Vendor
     */
    public function vendor(): BelongsTo
    {
        return $this->belongsTo(Vendor::class);
    }

    /**
     * An PurchaseOrder HasMany Products
     */
    public function products(): HasMany
    {
        return $this->hasMany(Product::class);
    }

    /**
     * BelongsTo Payment Methods
     */
    public function paymentMethod(): BelongsTo
    {
        return $this->belongsTo(PaymentMethod::class, 'payment_method_id', 'id');
    }

    /**
     * Check if the purchase order is currently is_default.
     */
    public function isDefault(): bool
    {
        return (bool) $this->status?->is_default;
    }

    /**
     * Check if the purchase order is currently is_editable.
     */
    public function isEditable(): bool
    {
        return (bool) $this->status?->is_editable;
    }

    /**
     * Check if the purchase order is currently is_final_step.
     */
    public function isFinalStep(): bool
    {
        return (bool) $this->status?->is_final_step;
    }

    /**
     * Check if the purchase order is currently archived.
     */
    public function isArchived(): bool
    {
        return (bool) $this->status?->archived;
    }

    /**
     * Updates the order status to is_processing or default based on the delivery logic.
     */
    public function setIsProcessing(bool $isProcessing = true): self
    {
        // Retrieves the correct ID from the OrderStatus model
        $statusId = $isProcessing
            ? PurchaseOrderStatus::getIsProcessingId()
            : PurchaseOrderStatus::getDefaultId();

        if ($statusId) {
            $this->update([
                'order_status_id' => $statusId,
            ]);
        }

        return $this;
    }

    /**
     * Updates the order status to is_final_step or default based on the delivery logic.
     */
    public function setIsFinalStep(bool $isFinalStep = true): self
    {
        // Retrieves the correct ID from the OrderStatus model
        $statusId = $isFinalStep
            ? PurchaseOrderStatus::getIsFinalStepId()
            : PurchaseOrderStatus::getDefaultId();

