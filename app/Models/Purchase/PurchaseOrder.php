    /**
     * Get the complete purchase orders list formatted for Filament Select components,
     * taking into account the user's role, multi-team tenancy, ownership, dynamic memberships,
     * and forcing the inclusion of the currently selected record.
     */
    public static function getOptionsForSelect(int|string|null $currentId = null): array|Collection
    {
        $user = auth()->user();

        // Prevent errors if no user is currently authenticated
        if (! $user) {
            return [];
        }

        // Base query setup with eager loading needed for the labels and models
        // Note: 'orderModel' assumes the relationship name for 'order_model_id'
        $baseQuery = PurchaseOrder::select(['id', 'team_id', 'user_id', 'number', 'date', 'order_model_id', 'total'])
            ->with(['team', 'defaultModel']);

        // Helper function to generate the custom dynamic label
        $generateLabel = function ($order) {
            $modelName = $order->defaultModel?->name ?? '';
            $formattedTotal = $order->total ? number_format($order->total, 2, ',', '.').UserService::getCurrencyPrefix() : '';
            $formattedDate = $order->date ? Carbon::parse($order->date)->format(auth()->user()->date_format) : '';

            return "Nr.:{$order->number} - {$formattedDate} ({$modelName}) - {$formattedTotal}";
        };

        // Standard user flow: filter by team, ownership, or explicit membership (if available)
        if (! $user->hasRole(['super_admin'])) {
            return $baseQuery->where('team_id', $user->team_id)
                ->where(function ($query) use ($user, $currentId) {
                    // Standard restriction path: must be owned or membered
                    $query->where(function ($subQuery) use ($user) {
                        // Standard user sees records bound to themselves
                        $subQuery->where('user_id', $user->id);

                        // Dynamic check: apply membership filter only if relation exists on the model
                        if (method_exists(PurchaseOrder::class, 'members')) {
                            $subQuery->orWhereHas('members', function ($memberQuery) use ($user) {
                                $memberQuery->where('user_id', $user->id);
                            });
                        }
                    });

                    // Historical fallback path: force-include the current record within tenant bounds
                    if ($currentId) {
                        $query->orWhere('id', $currentId);
                    }
                })
                ->get()
                ->mapWithKeys(function ($order) use ($generateLabel) {
                    return [$order->id => $generateLabel($order)];
                });
        }

        // Super admin flow: fetch purchase orders grouped by team name
        return $baseQuery->where(function ($query) use ($currentId) {
            // Force-include the current historical record for super admin as well
            if ($currentId) {
                $query->orWhere('id', $currentId);
            }
        })
            ->get()
            ->groupBy(function ($order) {
                return $order->team?->name ? 'Team '.$order->team->name : 'No Team';
            })
            ->map(function ($group) use ($generateLabel) {
                return $group->mapWithKeys(function ($order) use ($generateLabel) {
                    return [$order->id => $generateLabel($order)];
                });
            });
    }
}
