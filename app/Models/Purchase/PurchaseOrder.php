                'order_status_id' => $statusId,
            ]);
        }

        return $this;
    }

    /**
     * Updates the order status to archived or default based on the delivery logic.
     */
    public function setArchived(bool $isArchived = true): self
    {
        // Retrieves the correct ID from the OrderStatus model
        $statusId = $isArchived
